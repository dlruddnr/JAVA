package sec06.ch03;

public class OperatorExam {
	public static void main(String[] args) {
		int n1=10;
		int n2=3;
		System.out.println(n1+n2);
		System.out.println(n1-n2);
		System.out.println(n1/n2);
		System.out.println((float)n1/n2);
		System.out.println(n1*n2);
		
		int mod=10%3;
		System.out.println("mod : "+mod);
		int n3=-12;
		System.out.println(-n3);
	}
}

